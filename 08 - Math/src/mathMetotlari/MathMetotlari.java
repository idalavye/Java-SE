package mathMetotlari;

public class MathMetotlari {
	
	public static void main(String[] args) {
		
		
		/*
		 * 	Math.sqrt(sayi) yazilan sayinin kökünü alýr double türünde sonuc üretir.
		 * 
		 * 	Math.ceil(5.7) sayiyi bir üst sayiya yuvarlar.
		 * 
		 *  Math.pow(5, 3) girilen 1.parametre sayi, 2.parametra ise üssü örnek olarak : 5^3
		 *  
		 *  Math.max(20, 10); büyük olan parametreyi bulur.
		 *  
		 *  Math.min(15, 25); kücük olan parametreyi bulur
		 *  
		 *  Math.random() 0 ile 1 arasýnda rastgele bir sonuc üretir.
		 */
		
		double sayi  = 1 + Math.random() * 100;
		
		
		
		System.out.println(sayi);
		
		
	}
	

}
