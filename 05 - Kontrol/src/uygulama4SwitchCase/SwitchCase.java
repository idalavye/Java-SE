package uygulama4SwitchCase;

public class SwitchCase {

	public static void main(String[] args) {

		int a = 90;

		switch (a) {

		case 90:
			System.out.println("A 90 dýr");
			break;

		case 100:
			
			System.out.println("A 100 dür");
			
			
			break;
		case 150:	
			
			System.out.println("A 150 dir");
			break;
		default:
			System.out.println("A degerini bilmiyorum");
			break;
		}

	}

}
