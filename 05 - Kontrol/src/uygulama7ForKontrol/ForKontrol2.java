package uygulama7ForKontrol;

public class ForKontrol2 {
	
	public static void main(String[] args) {
		
			
		for(int x = 3, y = 5; x * y < 50 ; x++, y +=2 ){
			
			System.out.println("X'in degeri : " + x + ", Y nin degeri : " + y);
			
			
		}
		
		
		for(int x = 3, y = 5; x * y < 50 ; x++, y -=2 ){
			
			System.out.println("X'in degeri : " + x + ", Y nin degeri : " + y);
			
			
		}
		
		
	}

}
